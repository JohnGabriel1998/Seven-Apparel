/**
 * Get all regions
 */
export const getAllRegions = (): Region[] => {
  return philippineLocations;
};

/**
 * Get provinces by region code
 */
export const getProvincesByRegion = (regionCode: string): Province[] => {
  const region = philippineLocations.find((r) => r.code === regionCode);
  return region ? region.provinces : [];
};

/**
 * Get cities by province name
 */
export const getCitiesByProvince = (provinceName: string): City[] => {
  for (const region of philippineLocations) {
    const province = region.provinces.find((p) => p.name === provinceName);
    if (province) {
      return province.cities;
    }
  }
  return [];
};

/**
 * Get barangays by city name
 */
export const getBarangaysByCity = (cityName: string): Barangay[] => {
  for (const region of philippineLocations) {
    for (const province of region.provinces) {
      const city = province.cities.find((c) => c.name === cityName);
      if (city) {
        return city.barangays;
      }
    }
  }
  return [];
};

/**
 * Get complete location details by barangay
 */
export const getLocationDetails = (barangayName: string, cityName: string) => {
  for (const region of philippineLocations) {
    for (const province of region.provinces) {
      const city = province.cities.find((c) => c.name === cityName);
      if (city) {
        const barangay = city.barangays.find((b) => b.name === barangayName);
        if (barangay) {
          return {
            region: region.name,
            regionCode: region.code,
            province: province.name,
            city: city.name,
            cityType: city.type,
            barangay: barangay.name,
            zipCode: barangay.zipCode || city.zipCode,
          };
        }
      }
    }
  }
  return null;
};

/**
 * Search cities across all regions
 */
export const searchCities = (searchTerm: string): Array<{ city: City; province: string; region: string }> => {
  const results: Array<{ city: City; province: string; region: string }> = [];
  const lowerSearch = searchTerm.toLowerCase();

  for (const region of philippineLocations) {
    for (const province of region.provinces) {
      for (const city of province.cities) {
        if (city.name.toLowerCase().includes(lowerSearch)) {
          results.push({
            city,
            province: province.name,
            region: region.name,
          });
        }
      }
    }
  }

  return results;
};
