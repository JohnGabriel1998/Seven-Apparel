/**
 * Lookup city and province by ZIP code
 * @param zipCode - 4-digit Philippine ZIP code
 * @returns Object containing city and province, or null if not found
 */
export const lookupZipCode = (zipCode: string): ZipCodeData | null => {
  const trimmedZip = zipCode.trim();
  return philippineZipCodes[trimmedZip] || null;
};

/**
 * Validate Philippine ZIP code format
 * @param zipCode - ZIP code to validate
 * @returns true if valid format (4 digits)
 */
export const isValidZipCode = (zipCode: string): boolean => {
  const zipPattern = /^\d{4}$/;
  return zipPattern.test(zipCode.trim());
};

/**
 * Get all cities for a given province
 * @param province - Province name
 * @returns Array of city names
 */
export const getCitiesByProvince = (province: string): string[] => {
  const cities = new Set<string>();
  Object.values(philippineZipCodes).forEach((data) => {
    if (data.province === province) {
      cities.add(data.city);
    }
  });
  return Array.from(cities).sort();
};

/**
 * Get all provinces
 * @returns Array of province names
 */
export const getAllProvinces = (): string[] => {
  const provinces = new Set<string>();
  Object.values(philippineZipCodes).forEach((data) => {
    provinces.add(data.province);
  });
  return Array.from(provinces).sort();
};
